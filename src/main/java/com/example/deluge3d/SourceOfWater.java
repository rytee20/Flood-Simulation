package com.example.deluge3d;
public class SourceOfWater
{
	public int x;
	public int y;

	public int sizeX;
	public int sizeY;
	public float PowerOfSource;
	
	public SourceOfWater(int x, int y, int sizeX, int sizeY, float PowerOfSource) {
		this.x = x;
		this.y = y;
		this.sizeX = sizeX;
		this.sizeY = sizeY;
		this.PowerOfSource=PowerOfSource;
	}
}
